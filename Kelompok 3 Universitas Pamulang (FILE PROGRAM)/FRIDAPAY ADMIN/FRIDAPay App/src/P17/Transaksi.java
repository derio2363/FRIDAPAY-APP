    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
    String namaProduk = jTextField9.getText().trim();      
    String namaUser = jTextField10.getText().trim();        
    String namaPengirim = jTextField7.getText().trim();     
    String namaPenerima = jTextField8.getText().trim();     
    String jumlah = jTextField4.getText().trim();           
    String tanggal = jTextField5.getText().trim();          
    String keterangan = jTextField6.getText().trim();       

    // Validasi umum
    if (jumlah.isEmpty() || tanggal.isEmpty() || keterangan.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Jumlah, Tanggal, dan Keterangan wajib diisi!");
        return;
    }

    String tipe = jComboBox1.getSelectedItem().toString();
    
    try (Connection conn = DatabaseConnection.connect()) {
        String sql = "INSERT INTO transaksi (nama_produk, nama_user, nama_pengirim, nama_penerima, jumlah, tanggal, keterangan) VALUES (?, ?, ?, ?, ?, ?, ?)";
        PreparedStatement stmt = conn.prepareStatement(sql);

        if (tipe.equals("Transfer")) {
            stmt.setString(1, null); 
            stmt.setString(2, null); 
            stmt.setString(3, namaPengirim);
            stmt.setString(4, namaPenerima);
        } else {
            stmt.setString(1, namaProduk);
            stmt.setString(2, namaUser);
            stmt.setString(3, null); 
            stmt.setString(4, null);
        }

        stmt.setString(5, jumlah);
        stmt.setString(6, tanggal);
        stmt.setString(7, keterangan);
        stmt.executeUpdate();

        JOptionPane.showMessageDialog(this, "Transaksi berhasil disimpan.");
        tampilkanTransaksi(); 


        jTextField4.setText("");
        jTextField5.setText("");
        jTextField6.setText("");
        jTextField7.setText("");
        jTextField8.setText("");
        jTextField9.setText("");
        jTextField10.setText("");

    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Gagal menyimpan: " + ex.getMessage());
        ex.printStackTrace();
    }

    }//GEN-LAST:event_jButton1ActionPerformed

