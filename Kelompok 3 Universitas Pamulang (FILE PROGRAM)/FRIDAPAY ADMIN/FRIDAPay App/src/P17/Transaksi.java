        private void tampilkanTransaksi() {
    try {
        Connection conn = DatabaseConnection.connect();
        String sql = "SELECT nama_produk, nama_user, nama_pengirim, nama_penerima, jumlah, tanggal, keterangan FROM transaksi";
        PreparedStatement stmt = conn.prepareStatement(sql);
        ResultSet rs = stmt.executeQuery();

        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Nama Produk");
        model.addColumn("Nama User");
        model.addColumn("Nama Pengirim");
        model.addColumn("Nama Penerima");
        model.addColumn("Jumlah");
        model.addColumn("Tanggal");
        model.addColumn("Keterangan");

        while (rs.next()) {
            model.addRow(new Object[] {
                rs.getString("nama_produk"),
                rs.getString("nama_user"),
                rs.getString("nama_pengirim"),
                rs.getString("nama_penerima"),
                rs.getBigDecimal("jumlah"),
                rs.getString("tanggal"),
                rs.getString("keterangan")
            });
        }

        jTable1.setModel(model);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    
}
        jComboBox1.addItemListener(new ItemListener() {
    @Override
    public void itemStateChanged(ItemEvent e) {
        if (e.getStateChange() == ItemEvent.SELECTED) {
            String selected = jComboBox1.getSelectedItem().toString();

            CardLayout cl = (CardLayout) jPanel3.getLayout();
            if (selected.equals("Transfer")) {
                cl.show(jPanel3, "card2"); 
                jPanel4.setVisible(true);
                jPanel5.setVisible(false);
            } else if (selected.equals("Top Up")) {
                cl.show(jPanel3, "card2"); 
                jPanel4.setVisible(false);
                jPanel5.setVisible(true);
            }
        }
    }
});
        setLocationRelativeTo(null);
    }
    
